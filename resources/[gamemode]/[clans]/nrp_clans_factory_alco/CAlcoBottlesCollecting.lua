local SEARCHING_START_TS = 0

addEvent( "CAF:onClientCreateMarkers", true )
addEventHandler( "CAF:onClientCreateMarkers", resourceRoot, function( marker_cooldowns )
    for marker_id, conf in pairs( TRASH_MARKERS ) do
        conf.text = "ALT Взаимодействие"
        conf.keypress = "lalt"
        conf.radius = 1.5
        conf.color = { 255, 168, 0, 70 }

        local tpoint = TeleportPoint( conf )
        -- tpoint:SetDropImage( { ":nrp_shared/img/dropimage.png", 255, 255, 255, 255 } )
        -- tpoint:SetImage( ":nrp_clans/img/tags/band/-2.png" )
        -- tpoint.element:setData( "material", true, false )
            
        tpoint.PreJoin = function( self, player )
            return not isTimer( tpoint.cooldown_timer ) and getRealTimestamp( ) - SEARCHING_START_TS >= TRASH_SEARCHING_DURATION
        end
            
        tpoint.PostJoin = function( self, player )
            SEARCHING_START_TS = getRealTimestamp( )
            triggerServerEvent( "CAF:onPlayerSearchTrashMarker", resourceRoot, marker_id )
        end

        tpoint.SetCooldown = function( self, cooldown_ts )
            if cooldown_ts and cooldown_ts <= getRealTimestamp( ) then
                tpoint.element.interior = 0
                return
            end

            tpoint.element.interior = 11 -- Скрываем маркер

            if isTimer( tpoint.cooldown_timer ) then tpoint.cooldown_timer:destroy( ) end
            tpoint.cooldown_timer = setTimer( function( )
                if not isElement( tpoint.element ) then return end
                tpoint.element.interior = 0 -- Показываем обратно
            end, ( cooldown_ts and ( cooldown_ts - getRealTimestamp( ) ) or TRASH_MARKER_COOLDOWN ) * 1000, 1 )
        end

        if marker_cooldowns[ marker_id ] then
            tpoint:SetCooldown( marker_cooldowns[ marker_id ] )
        end
    end
end )

addEvent( "CAF:onClientCooldownMarker", true )
addEventHandler( "CAF:onClientCooldownMarker", resourceRoot, function( marker_id )
    if not isElement( TRASH_MARKERS[ marker_id ].element ) then return end
    TRASH_MARKERS[ marker_id ]:SetCooldown( )
end )